int	ft_sqrt(int nb)
{
	int	i;
	int	c;
	int	y;

	i = 1;
	y = nb;
	c = 1;
	while (i <= nb)
	{
		y -= i;
		i += 2;
		if (y == 0)
			return (c);
		if (y < 0)
			return (0);
		c++;
	}
	return (0);
}

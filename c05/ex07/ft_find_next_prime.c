#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (1)
	{
		if (ft_is_prime(nb) && nb <= 2147483647)
			return (nb);
		nb++;
	}
}

int	main(void)
{
	printf("%d", ft_find_next_prime(2000000000));
	return (0);
}
